%%% characteristic function in 3-D

function f = fUn3(x,y,z,u,v,w,a1,a2,a3,b1,b2,b3,c1,c2,c3,d,s1,s2,s3,s4,s5,s6)
D=0.05;
f = cos(u.*x+v.*y+w.*z-D^(1.5)*(a1.*(x.^3)+a2.*(y.^3)+a3.*(z.^3)...
    +3.*b1.*(x.^2).*y+3.*b2.*(y.^2).*z+3.*b3.*(z.^2).*x...
    +3.*c1.*x.*(y.^2)+3*c2.*y.*(z.^2)+3.*c3.*z.*(x.^2)...
    +6*d.*x.*y.*z)/6).*...
    exp((-D.*(s1.*(x.^2)+s2.*(y.^2)+s3.*(z.^2)...
    +2.*s4.*x.*y+2.*s5.*x.*z+2.*s6.*y.*z))/2)/(2*pi)^3;
end